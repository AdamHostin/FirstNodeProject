import mongoose from "mongoose"

const TaskSchema = mongoose.Schema({
    description: {
        type: String,
        required: true,
        trim: true
    },
    completed: {
        type: Boolean,
        default: false
    },
    owner: {
        type: mongoose.Schema.Types.ObjectId,
        required: true,
        ref: 'User'
    }
},{
    timestamps: true
})

const TaskModel = mongoose.model('Task', TaskSchema)
TaskModel.createIndexes()

export const Task = TaskModel